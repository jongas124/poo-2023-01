package com.github.joseppe.t08.Curva;

public class Ponto {
    private double x;
    private double y;

    public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // getters e setters

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
}